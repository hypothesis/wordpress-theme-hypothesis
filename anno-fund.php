                distributed as grants with only limited
                obligations.
                </p>
                <p>
                Applications do not need to relate to any particular
                annotation platform or technology, but only to generally
                integrate or provide annotation functionality and be open
                source in nature.
                </p>
            </div>
            <div class="col-md-4">
                <h4 class="service-heading">The Grants</h4>
                <p>
